export const filterPlaces = (array, searchTag, result) => {
    for (var i = 0; i < array.length; i++) {
        let lugar = array[i];
        if (lugar.tags.includes(searchTag)) {
          result.push(lugar);
        } 
    }

    return result;
}

export const getPlace = (array, placeName, result) => {
    for (var i = 0; i < array.length; i++) {
        let lugar = array[i];
        if (lugar.tags.includes(placeName)) {
          result.push(lugar);
        } 
    }

    return result;
}

export const getReviews = (array, result) => {
    for (var i = 0; i < array.length; i++) {
        let lugar = array[i];
        for (var j = 0; j < lugar.reviews.length; j++) {
            result.push(lugar.reviews[j])
        }
    }

    return result;
}

